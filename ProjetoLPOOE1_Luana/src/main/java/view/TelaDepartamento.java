    private void listarDepartamentos() {
    JLDepartamento.clearSelection();
    jpa.conexaoAberta();
    List<Departamentos> dep = jpa.getDepartamentos();
    DefaultListModel<Departamentos> mod = new DefaultListModel<>();
    for (Departamentos d : dep) {
        mod.addElement(d);
    }
    JLDepartamento.setModel(mod);
    jpa.fecharConexao();
}


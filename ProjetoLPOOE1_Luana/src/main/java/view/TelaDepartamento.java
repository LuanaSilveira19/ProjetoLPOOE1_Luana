    private void BTRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTRemoverActionPerformed
          Departamentos departamentoSelecionado = JLDepartamento.getSelectedValue();
        if (departamentoSelecionado != null) {
            int confirmacaoDel = JOptionPane.showConfirmDialog(rootPane,
                    "Tem certeza que deseja remover o departamento " + departamentoSelecionado.getNome());
            if (confirmacaoDel == JOptionPane.YES_OPTION) {
                try {

                    jpa.conexaoAberta();
                    jpa.remover(departamentoSelecionado);
                   jpa.fecharConexao();
                    listarDepartamentos();

                } catch (Exception e) {
                    System.err.println("Erro ao excluir funcionario: " + e.getMessage());
                } finally {
                    jpa.fecharConexao();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Nenhum departamento selecionado");
        }
    }//GEN-LAST:event_BTRemoverActionPerformed


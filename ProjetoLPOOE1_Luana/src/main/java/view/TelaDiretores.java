    private void BTEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTEditarActionPerformed
        Diretores diretorSelecionado
                = JLDiretores.getSelectedValue();
        if (diretorSelecionado != null) {

            try {

                jpa.conexaoAberta();

                Diretores diretorPersistido = (Diretores) jpa.find(Diretores.class, diretorSelecionado.getId());
                diretorPersistido.setNome(JOptionPane.showInputDialog(rootPane, "Informe o novo nome do diretor: ", diretorSelecionado.getNome()));
                jpa.persist(diretorPersistido);
                jpa.fecharConexao();
                listarDiretores();

            } catch (Exception e) {
                System.err.println("Erro ao editar diretor: " + e.getMessage());
            } finally {
                jpa.fecharConexao();
            }

        }
    }//GEN-LAST:event_BTEditarActionPerformed

    private void BTAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTAdicionarActionPerformed
       
        Diretores d = new Diretores();


        String nome = JOptionPane.showInputDialog("Informe o nome do diretor:");
        if (nome == null || nome.trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Nome não pode ser vazio");
            return;
        }
        d.setNome(nome);


        String cargo = JOptionPane.showInputDialog("Informe o cargo do diretor:");
        if (cargo == null || cargo.trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Cargo não pode ser vazio");
            return; 
        }
        d.setCargo(cargo);


        String endereco = JOptionPane.showInputDialog("Informe o endereço do diretor:");
        if (endereco == null || endereco.trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Endereço não pode ser vazio");
            return; 
        }
        d.setEndereco(endereco);


        String data_nasc = JOptionPane.showInputDialog("Informe a data de nascimento do diretor:");
        if (data_nasc == null || data_nasc.trim().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Data de nascimento não pode ser vazia");
            return; 
        }
        d.setData_nasc(data_nasc);


        String salarioStr = JOptionPane.showInputDialog("Informe o salário do diretor:");
        int salario;
        try {
            salario = Integer.parseInt(salarioStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Salário deve ser um número válido");
            return; 
        }
        d.setSalario(salario);

        try {
            jpa.conexaoAberta();
            jpa.persist(d);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao adicionar diretor: " + e.getMessage());
        } finally {
            jpa.fecharConexao();
        }


        JOptionPane.showMessageDialog(rootPane, "Diretor adicionado");


        listarDiretores();


    }//GEN-LAST:event_BTAdicionarActionPerformed

    private void BTRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTRemoverActionPerformed
       
        
        Diretores diretorSelecionado = JLDiretores.getSelectedValue();
          // Verifica se o funcionário está associado a um departamento
    boolean estaAssociado = jpa.existeAssociacaoDiretor(diretorSelecionado);
    
    if (estaAssociado) {
        JOptionPane.showMessageDialog(rootPane,
                "O diretor não pode ser removido porque está associado a um departamento.");
        return; // Interrompe a execução do método
    }
    
        if (diretorSelecionado != null) {
            int confirmacaoDel = JOptionPane.showConfirmDialog(rootPane,
                    "Tem certeza que deseja remover o diretor " + diretorSelecionado.getNome());//ver se ele está ligado a um departamento
            if (confirmacaoDel == JOptionPane.YES_OPTION) {
                try {

                    jpa.conexaoAberta();
                    jpa.remover(diretorSelecionado);
                    jpa.fecharConexao();
                    listarDiretores();

                } catch (Exception e) {
                    System.err.println("Erro ao excluir diretor: " + e.getMessage());
                } finally {
                    jpa.fecharConexao();
                }
            }
        } else {
            JOptionPane.showMessageDialog(rootPane,
                    "Nenhum funcionario selecionado");
        }
    }//GEN-LAST:event_BTRemoverActionPerformed


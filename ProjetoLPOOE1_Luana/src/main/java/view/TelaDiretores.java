    private void listarDiretores() {
        JLDiretores.clearSelection();
        jpa.conexaoAberta();
        List<Diretores> diretores = jpa.getDiretores();
        DefaultListModel<Diretores> mod = new DefaultListModel<>();
        for (Diretores d : diretores) {
            mod.addElement(d);
        }
        JLDiretores.setModel(mod);
        jpa.fecharConexao();
    }

